#include "monty.h"

/**
 * addOP - performs add op
 *
 * @stack: the list
 * @line_num: line number.
 * @opcode: to keep opcode the same.
 * @fd: the file
 *
 */
void addOP(stack_t **stack, unsigned int line_num, char *opcode, FILE *fd)
{
	stack = stack;
	line_num = line_num;
	opcode = opcode;
	fd = fd;
}
